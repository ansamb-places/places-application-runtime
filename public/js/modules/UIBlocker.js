define([], function() {
  var overlay;
  var loading_icon;
  overlay = "<div class='p-freeze'><div class='p-middle'></div></div>";
  loading_icon= "<span data-icon='l' class='p-loading p-color-place p-big'></span>"
  return {
    freeze: function(message,load,sp_el) {
      var $el;
      $el= $(overlay);
      $container=$el.find(".p-middle")
      if(load){$container.append(loading_icon);};
      if(message){$container.append("<span class='p-message'>"+message+"</span>");};
      if(sp_el){$container.append(sp_el);};
      this.disabledKeyboard();
      return $('body').append($el);
    },
    unfreeze: function() {
      this.enabledKeyboard();
      return $('.p-freeze').remove();
    },
    disabledKeyboard:function(){
      $(window).on("keydown",function(e){
        e.stopPropagation();
        e.preventDefault();
        return
      })
    },
    enabledKeyboard:function(){
      $(window).off("keydown").on("keydown",function(e){return true;});
    }
  };
});
